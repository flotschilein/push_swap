#include "libft.h"

int	ft_sqrt(int num)
{
	int	i;

	i = 2;
	if (num < 4)
		return (1);
	while (i <= num / i)
	{
		if (i * i == num)
			return (i);
		i++;
	}
	return (i - 1);
}
